package alexman.dndboard.entity;

/**
 * Represents the different states a Character may be in. Each state is
 * typically associated with a different sprite to display.
 *
 * @author Alex Mandelias
 */
public enum CharacterState {

	/** Idle state */
	IDLE,

	/** Down state */
	DOWN,

	/** Dead state */
	DEAD;
}
